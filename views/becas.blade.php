  <h4 class="display-6 font-italic font-weight-bold">Compromiso de la persona becaria.</h4>
  <p>Aquella persona que se vea beneficiada de un beca para su participación en el X ECSL 2019 se compromete ser voluntario en actividades durante el evento por medio de:</p>
  <ul>
    <li>Impartir una charla o taller en relación a los ejes temáticos del ECSL.</li>
    <li>A su participación activa en los diversos eventos que se realicen durante el ECSL.</li>
    <li>Colaborar con las solicitudes de publicidad que sean necesarias para la documentación del evento, solicitadas por el comité organizador del ECSL.</li>
  </ul>
  <h4 class="display-6 font-italic font-weight-bold">Criterios de selección.</h4>
