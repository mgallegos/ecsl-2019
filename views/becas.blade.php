  <ul>
    <li>Estar completamente de acuerdo con todos los puntos del compromiso de la persona becaria de la selección anterior.</li>
    <li>Como postulante es extranjero a El Salvador y parte del resto de la región centroamericana.</li>
    <li>Haber obtenido el correo de confirmación de recepción de solicitud durante las siguientes 24 horas desde el momento de llenado del correo.</li>
