  <h3 class="font-weight-bold">Becas ECSL 2019.</h3>
  <p>
    Para este año se ha definido otorgar un total de 10 becas de un valor individual de $150.00 USD únicamente para participantes extranjeros es importante aclarar que esta beca no incluye el costo de traslado del país de origen hacia El Salvador.
